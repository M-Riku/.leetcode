# [406] Queue Reconstruction by Height
#

# @lc code=start
class Solution:
    def reconstructQueue(self, people: List[List[int]]) -> List[List[int]]:
        people.sort(key=lambda x: (-x[0], x[1]))
        result = []

        for i in range(len(people)):
            result.insert(people[i][1], people[i])

        return result
# @lc code=end
